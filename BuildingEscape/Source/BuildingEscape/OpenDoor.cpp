// Called when the game starts
void UOpenDoor::BeginPlay()
{
	Super::BeginPlay();

	FString GetRotation = GetOwner()->GetActorRotation().ToString();
	FString GetName = GetOwner()->GetName();
	UE_LOG(LogTemp, Warning, TEXT("%s has a rotation of %s"), *GetName, *GetRotation);
	FRotator NewRotation = FRotator(0.0f,90.0f,0.0f);
	GetOwner()->SetActorRotation(NewRotation);
}


